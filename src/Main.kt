//import java.io.IO.println

fun main() {
    // Numeros
    val x: Int = 2
    println("Entero: $x")

    val y: Float = 2.5F
    println("Flotante: $y")
    //Alfanumericos
    val caracter: Char = 'A'
    println("Caracter: $caracter")

    val string: String = "ABC"
    println("String: $string")

    // Booleanos
    val booleano: Boolean = true
    println("Booleano: $booleano")
}